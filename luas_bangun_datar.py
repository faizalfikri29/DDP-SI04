def l_persegi(sisi):
    luas = sisi * sisi
    print((f'Luas Persegi adalah {sisi} * {sisi} = {luas}'))

def l_persegipanjang(panjang, lebar):
    luas = panjang*lebar
    print(f'Luas Persegi panjang {panjang} * {lebar} = {luas}')

def l_segitiga(alas, tinggi):
    luas = 0.5 * alas * tinggi
    print(f'Luas Segitiga 1/2 * {alas} * {tinggi}')

def l_lingkaran(r1, r2):
    luas = 3.14 * r1 * r2
    print(f'Luas lingkaran adalah phi * {r1} * {r2} = {luas}')

def l_JajarGenjang(alas, tinggi):
    luas = alas * tinggi
    print(f'Luas Janjang Genjang {alas} * {tinggi} = {luas}')